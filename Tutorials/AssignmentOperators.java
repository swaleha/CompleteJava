public class AssignmentOperators {
    public static void main(String args[]){
        int A = 10;
        System.out.println(A+=10); //A = A + 10
        System.out.println(A-=10); //A = A - 10
        System.out.println(A*=10); //A = A * 10
        System.out.println(A/=10); //A = A / 10
        System.out.println(A%=10); //A = A % 10
    }
    
}
